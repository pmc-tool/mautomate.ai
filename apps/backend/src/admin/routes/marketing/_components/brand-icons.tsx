const FALLBACK: BrandDef = { label: "Channel", color: "#6B7280", path: "" }

export const brandDef = (platform: string): BrandDef =>
  BRANDS[(platform || "").toLowerCase()] ?? { ...FALLBACK, label: platform || "Channel" }

/** The bare brand glyph, coloured by the brand (or `color` override). */
export function BrandGlyph({
  platform,
  size = 18,
  color,
  title,
}: {
  platform: string
  size?: number
  color?: string
  title?: string
}) {
  const def = brandDef(platform)
  if (!def.path) {
    // Generic channel dot for unknown platforms.
    return (
      <svg width={size} height={size} viewBox="0 0 24 24" role="img" aria-label={title ?? def.label}>
        <circle cx="12" cy="12" r="9" fill={color ?? def.color} />
      </svg>
    )
  }
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      role="img"
      aria-label={title ?? def.label}
      fill={color ?? def.color}
    >
      <title>{title ?? def.label}</title>
      <path d={def.path} />
    </svg>
  )
}

/**
 * A rounded tile carrying the brand glyph on a tint of the brand colour — the
 * default way to show a platform in lists, cards and headers.
 */
export function BrandIcon({
  platform,
  size = 32,
  className,
}: {
  platform: string
  size?: number
  className?: string
}) {
  const def = brandDef(platform)
  const glyph = Math.round(size * 0.56)
  return (
    <span
      className={className}
      style={{
        display: "inline-flex",
        alignItems: "center",
        justifyContent: "center",
        width: size,
        height: size,
        borderRadius: Math.round(size * 0.28),
        background: `color-mix(in srgb, ${def.color} 14%, transparent)`,
        boxShadow: `inset 0 0 0 1px color-mix(in srgb, ${def.color} 22%, transparent)`,
        flexShrink: 0,
      }}
      title={def.label}
    >
      <BrandGlyph platform={platform} size={glyph} />
    </span>
  )
}

/** A compact pill: brand glyph + label, used inline on posts/targets. */
export function BrandBadge({
  platform,
  label,
  size = 14,
}: {
  platform: string
  label?: boolean
  size?: number
}) {
  const def = brandDef(platform)
  return (
    <span
      style={{
        display: "inline-flex",
        alignItems: "center",
        gap: 5,
        padding: label ? "2px 8px 2px 6px" : 4,
        borderRadius: 999,
        background: `color-mix(in srgb, ${def.color} 12%, transparent)`,
        color: `color-mix(in srgb, ${def.color} 72%, var(--fg, #111))`,
        fontSize: 11,
        fontWeight: 600,
        lineHeight: 1,
      }}
      title={def.label}
    >
      <BrandGlyph platform={platform} size={size} />
      {label && <span>{def.label}</span>}
    </span>
  )
}
